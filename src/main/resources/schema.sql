create table customer (id bigint generated by default as identity, first_name varchar(255), last_name varchar(255), user_name varchar(255), primary key (id));
create table location (id bigint generated by default as identity, city varchar(255), country varchar(255), county varchar(255), street varchar(255), name varchar(255), primary key (id));
create table product (id bigint generated by default as identity, description varchar(255), name varchar(255), price decimal(19,2), weight double, product_category_id bigint not null, stock_id bigint not null, supplier_id bigint not null, primary key (id));
create table product_category (id bigint generated by default as identity, description varchar(255), name varchar(255), primary key (id));
create table stock (id bigint generated by default as identity, location integer, product integer, quantity integer, primary key (id));
create table supplier (id bigint generated by default as identity, name varchar(255), primary key (id));
create table order_table (id bigint generated by default as identity, city varchar(255), country varchar(255), county varchar(255), street varchar(255), order_date timestamp, order_id integer, product integer, quantity integer, shipped_from integer, customer_id bigint not null, primary key (id));
create table order_order_detail (order_id bigint not null, product integer, quantity integer);
create table revenue (id bigint generated by default as identity, date timestamp, sum decimal(19,2), location_id bigint not null, primary key (id));

